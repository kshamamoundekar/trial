package star;

public class Triangle2 {
	public static void main(String[] args) {
		
	
	int space = 9,starA=1;
	for(int line1 =1;line1<=10;line1++) {
		for(int space1=1;space1<=space;space1++) {
		System.out.print(" ");}
		for(int star2=1;star2<=starA;star2++) {
		System.out.print("*");}
		System.out.println();
		space--;
		starA++;}}
	}
