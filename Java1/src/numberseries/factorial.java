package numberseries;

public class factorial {
	
	public static void main(String[] args) {


		int A=1;
		int Z=8;
		int save=1;
		for(int i=A;i<=Z;i++) {
			save=save*i;
			System.out.println(save);
		}

		int a= 1;
		int b=8;
		int sav = 1;
		for(int z=a;z<=b;z++) {
			sav=save*z;
		}
	
	}

}
